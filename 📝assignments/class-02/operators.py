# 1. Addition
a = 5
b = 3
result = a + b
print("Addition:", result)

# 2. Subtraction
result = a - b
print("Subtraction:", result)

# 3. Multiplication
result = a * b
print("Multiplication:", result)

# 4. Division
result = a / b
print("Division:", result)

# 5. Modulus
result = a % b
print("Modulus:", result)

# 6. Exponentiation
result = a ** b
print("Exponentiation:", result)

# 7. Floor Division
result = a // b
print("Floor Division:", result)

# 8. Comparison Operators
print("Equal:", a == b)
print("Not Equal:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater than or equal to:", a >= b)
print("Less than or equal to:", a <= b)

# 9. Logical Operators
x = True
y = False
print("Logical AND:", x and y)
print("Logical OR:", x or y)
print("Logical NOT:", not x)

# 10. Bitwise Operators
c = 6  # Binary: 110
d = 2  # Binary: 010
print("Bitwise AND:", c & d)
print("Bitwise OR:", c | d)
print("Bitwise XOR:", c ^ d)
print("Bitwise NOT:", ~c)
print("Bitwise Left Shift:", c << 1)
print("Bitwise Right Shift:", c >> 1)